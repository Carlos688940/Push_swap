#include "ft_printf.h"

int	ft_str_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		return (ft_str_fd("(null)", 1));
	while (s[i])
	{
		write (fd, &s[i], 1);
		i++;
	}
	return (i);
}
